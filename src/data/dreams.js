export function getDreamByLetter(letter) {
  return dreamSymbols.filter(d => d.letter === letter)
}

export function searchDreams(query) {
  const q = query.toLowerCase()
  return dreamSymbols.filter(d =>
    d.title.toLowerCase().includes(q) ||
    d.shortDesc.toLowerCase().includes(q) ||
    d.tags.some(t => t.toLowerCase().includes(q))
  )
}

export const alphabet = [...new Set(dreamSymbols.map(d => d.letter))].sort()
